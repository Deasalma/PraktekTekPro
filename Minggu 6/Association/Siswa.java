package Association;

public class Siswa {
	private String nama;
	private int kelas;
	
	Siswa(String nama){
		this.nama = nama;
	}
	public String getNama() {
		return nama;
	}
	public int getKelas() {
		return kelas;
	}
	public void setKelas(int kelas) {
		this.kelas = kelas;
	}

}
